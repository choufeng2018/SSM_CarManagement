package com.entity;

//月统计
public class Yue  {

	private int id;//主键
	
	private String month;//月份
	
	private Double jine;//租金
	
	private int weihu;//维护费

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public Double getJine() {
		return jine;
	}

	public void setJine(double d) {
		this.jine = d;
	}

	public int getWeihu() {
		return weihu;
	}

	public void setWeihu(int weihu) {
		this.weihu = weihu;
	}

	
	
	
	
	
}
