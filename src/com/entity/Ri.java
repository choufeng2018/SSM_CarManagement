package com.entity;

//日统计
public class Ri  {

	private int id;//主键
	
	private String ri;//日期
	
	private Double jine;//租金
	
	private int weihu;//维护费

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getRi() {
		return ri;
	}

	public void setRi(String ri) {
		this.ri = ri;
	}

	public Double getJine() {
		return jine;
	}

	public void setJine(Double double1) {
		this.jine = double1;
	}

	public int getWeihu() {
		return weihu;
	}

	public void setWeihu(int weihu) {
		this.weihu = weihu;
	}
	
	
	
}
